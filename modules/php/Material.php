    public function getTokensWithPrefix(string $prefix) {
        $tokenTypes = $this->get();
        $res = [];
        foreach ($tokenTypes as $key => $info) {
            if (!str_starts_with($key, $prefix)) {
                continue;
            }

            $res[$key] = $info;
        }
        return $res;
    }

